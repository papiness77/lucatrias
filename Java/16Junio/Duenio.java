public class Duenio{
    private int edad;
    private int dni;
    private String nombre;
    public Duenio(){
    }
    public int getEdad() {
        return edad;
    }

    public void setDuenio(String nombre,int edad,int dni) {
        this.edad = edad;
        this.nombre= nombre;
        this.dni=dni;
    }

    public int getDni() {
        return dni;
    }
    public String getNombre() {
        return nombre;
    }
}